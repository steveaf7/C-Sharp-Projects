    //create a class
    class Math
    {
        //create three methods that take an integer, perform some math operation, and return an integer. 
        public static int Subtract20(int number)
        {
            number -= 20;
            return number;
        }
        public static int Times500(int number)
        {
            number *= 500;
            return number;
        }
        public static int DivideBy50(int number)
        {
            number /= 50;
            return number;
        }
    }
